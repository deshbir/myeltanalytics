package myeltanalytics.model;


public class MilestoneInfo {
	private String name;
	private String expiry;
	private String passPercent;
	private String passAction;
	private String progresstestcode;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getExpiry() {
		return expiry;
	}
	public void setExpiry(String expiry) {
		this.expiry = expiry;
	}
	public String getPassPercent() {
		return passPercent;
	}
	public void setPassPercent(String passPercent) {
		this.passPercent = passPercent;
	}
	public String getPassAction() {
		return passAction;
	}
	public void setPassAction(String passAction) {
		this.passAction = passAction;
	}
	public String getProgresstestcode() {
		return progresstestcode;
	}
	public void setProgresstestcode(String progresstestcode) {
		this.progresstestcode = progresstestcode;
	}
	
	
}
